public class Car {

    String id, brand, model, color, registrationNumber;
    int productionYear;
    double cost;


    public Car(String id,
               String brand,
               String model,
               int productionYear,
               String color,
               double cost,
               String registrationNumber) {
    this.id = id;
    this.brand = brand;
    this.model = model;
    this.productionYear = productionYear;
    this.color = color;
    this.cost =  cost;
    this. registrationNumber = registrationNumber;
    }

}
