public class Point {

    private int coordinateX;
    private int CoordinateY;

    Point (int x, int y) {
        this.coordinateX = x;
        this.CoordinateY = y;
    }

    public int getCoordinateX() {
        return coordinateX;
    }

    public int getCoordinateY() {
        return CoordinateY;
    }
}
