public class Point {

    private double coordinateX;
    private double CoordinateY;

    Point (double x, double y) {
        this.coordinateX = x;
        this.CoordinateY = y;
    }

    public double getCoordinateX() {
        return coordinateX;
    }

    public double getCoordinateY() {
        return CoordinateY;
    }
}
