package ua.hillel.javaElementary.HW09.triangle.model;

public class Point {

    private double coordinateX;
    private double CoordinateY;

    public Point (double x, double y) {
        this.coordinateX = x;
        this.CoordinateY = y;
    }

    public double getCoordinateX() {
        return coordinateX;
    }

    public double getCoordinateY() {
        return CoordinateY;
    }

}
